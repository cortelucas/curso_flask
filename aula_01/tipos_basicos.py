# tipos escaláveis
inteiro: int = 5
flutuante: float = 6.14
booleano: bool = True

# tipos compostos
texto: str = "Olá!"

print(
    f"O número {inteiro} é um {type(inteiro)}\n"
    f"O número {flutuante} é um {type(flutuante)}\n"
    f"O número {booleano} é um {type(booleano)}\n"
    f"O texto {texto} é um {type(texto)}"
)
